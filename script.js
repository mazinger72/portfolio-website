$(document).ready(function() {
	$(".social-icons li img").hover(function() {
        $(this).css("width", "9em");
        $(this).css("min-height", "100px")
	}, function() {
        $(this).css("width", "70px");
        $(this).css("min-height", "70px")
	});
});
